namespace HtmlValues.Attributes;

/// <summary>
/// Provides access to the attributes of the input element.
/// </summary>
public class InputAttributes : GlobalAttributes
{
    /// <summary>
    /// Gets the name of the accept attribute.
    /// </summary>
    public string Accept => HtmlAttributes.Accept;

    /// <summary>
    /// Gets the name of the alt attribute.
    /// </summary>
    public string Alt => HtmlAttributes.Alt;

    /// <summary>
    /// Gets the name of the autocomplete attribute.
    /// </summary>
    public string AutoComplete => HtmlAttributes.AutoComplete;

    /// <summary>
    /// Gets the name of the autofocus attribute.
    /// </summary>
    public string AutoFocus => HtmlAttributes.AutoFocus;

    /// <summary>
    /// Gets the name of the <c>capture</c> attribute.
    /// </summary>
    public string Capture => HtmlAttributes.Capture;

    /// <summary>
    /// Gets the name of the checked attribute.
    /// </summary>
    public string Checked => HtmlAttributes.Checked;

    /// <summary>
    /// Gets the name of the dirname attribute.
    /// </summary>
    public string DirName => HtmlAttributes.DirName;

    /// <summary>
    /// Gets the name of the disabled attribute.
    /// </summary>
    public string Disabled => HtmlAttributes.Disabled;

    /// <summary>
    /// Gets the name of the form attribute.
    /// </summary>
    public string Form => HtmlAttributes.Form;

    /// <summary>
    /// Gets the name of the formaction attribute.
    /// </summary>
    public string FormAction => HtmlAttributes.FormAction;

    /// <summary>
    /// Gets the name of the formenctype attribute.
    /// </summary>
    public string FormEncType => HtmlAttributes.FormEncType;

    /// <summary>
    /// Gets the name of the formmethod attribute.
    /// </summary>
    public string FormMethod => HtmlAttributes.FormMethod;

    /// <summary>
    /// Gets the name of the formnovalidate attribute.
    /// </summary>
    public string FormNoValidate => HtmlAttributes.FormNoValidate;

    /// <summary>
    /// Gets the name of the formtarget attribute.
    /// </summary>
    public string FormTarget => HtmlAttributes.FormTarget;

    /// <summary>
    /// Gets the name of the height attribute.
    /// </summary>
    public string Height => HtmlAttributes.Height;

    /// <summary>
    /// Gets the name of the list attribute.
    /// </summary>
    public string List => HtmlAttributes.List;

    /// <summary>
    /// Gets the name of the max attribute.
    /// </summary>
    public string Max => HtmlAttributes.Max;

    /// <summary>
    /// Gets the name of the maxlength attribute.
    /// </summary>
    public string MaxLength => HtmlAttributes.MaxLength;

    /// <summary>
    /// Gets the name of the min attribute.
    /// </summary>
    public string Min => HtmlAttributes.Min;

    /// <summary>
    /// Gets the name of the minlength attribute.
    /// </summary>
    public string MinLength => HtmlAttributes.MinLength;

    /// <summary>
    /// Gets the name of the multiple attribute.
    /// </summary>
    public string Multiple => HtmlAttributes.Multiple;

    /// <summary>
    /// Gets the name of the name attribute.
    /// </summary>
    public string Name => HtmlAttributes.Name;

    /// <summary>
    /// Gets the name of the pattern attribute.
    /// </summary>
    public string Pattern => HtmlAttributes.Pattern;

    /// <summary>
    /// Gets the name of the placeholder attribute.
    /// </summary>
    public string Placeholder => HtmlAttributes.Placeholder;

    /// <summary>
    /// Gets the name of the readonly attribute.
    /// </summary>
    public string ReadOnly => HtmlAttributes.ReadOnly;

    /// <summary>
    /// Gets the name of the required attribute.
    /// </summary>
    public string Required => HtmlAttributes.Required;

    /// <summary>
    /// Gets the name of the size attribute.
    /// </summary>
    public string Size => HtmlAttributes.Size;

    /// <summary>
    /// Gets the name of the src attribute.
    /// </summary>
    public string Src => HtmlAttributes.Src;

    /// <summary>
    /// Gets the name of the step attribute.
    /// </summary>
    public string Step => HtmlAttributes.Step;

    /// <summary>
    /// Gets the name of the type attribute.
    /// </summary>
    public string Type => HtmlAttributes.Type;

    /// <summary>
    /// Gets the name of the value attribute.
    /// </summary>
    public string Value => HtmlAttributes.Value;

    /// <summary>
    /// Gets the name of the width attribute.
    /// </summary>
    public string Width => HtmlAttributes.Width;
}