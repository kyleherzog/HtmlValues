namespace HtmlValues.AttributeValues;

/// <summary>
/// Provides access to predefined values for the <c>scope</c> attribute.
/// </summary>
public class ScopeValues
{
    /// <summary>
    /// Gets the predefined <c>col</c> attribute value.
    /// </summary>
    public string Col => HtmlAttributeValues.Col;

    /// <summary>
    /// Gets the predefined <c>colgroup</c> attribute value.
    /// </summary>
    public string ColGroup => HtmlAttributeValues.ColGroup;

    /// <summary>
    /// Gets the predefined <c>row</c> attribute value.
    /// </summary>
    public string Row => HtmlAttributeValues.Row;

    /// <summary>
    /// Gets the predefined <c>rowgroup</c> attribute value.
    /// </summary>
    public string RowGroup => HtmlAttributeValues.RowGroup;
}