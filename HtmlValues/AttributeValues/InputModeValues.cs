namespace HtmlValues.AttributeValues;

/// <summary>
/// Provides access to predefined values for the <c>inputmode</c> attribute.
/// </summary>
public class InputModeValues
{
    /// <summary>
    /// Gets the predefined <c>decimal</c> attribute value.
    /// </summary>
    public string Decimal => HtmlAttributeValues.Decimal;

    /// <summary>
    /// Gets the predefined <c>email</c> attribute value.
    /// </summary>
    public string Email => HtmlAttributeValues.Email;

    /// <summary>
    /// Gets the predefined <c>none</c> attribute value.
    /// </summary>
    public string None => HtmlAttributeValues.None;

    /// <summary>
    /// Gets the predefined <c>numeric</c> attribute value.
    /// </summary>
    public string Numeric => HtmlAttributeValues.Numeric;

    /// <summary>
    /// Gets the predefined <c>search</c> attribute value.
    /// </summary>
    public string Search => HtmlAttributeValues.Search;

    /// <summary>
    /// Gets the predefined <c>tel</c> attribute value.
    /// </summary>
    public string Tel => HtmlAttributeValues.Tel;

    /// <summary>
    /// Gets the predefined <c>text</c> attribute value.
    /// </summary>
    public string Text => HtmlAttributeValues.Text;

    /// <summary>
    /// Gets the predefined <c>url</c> attribute value.
    /// </summary>
    public string Url => HtmlAttributeValues.Url;
}