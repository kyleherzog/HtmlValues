namespace HtmlValues.AttributeValues;

/// <summary>
/// Provides access to predefined values for the <c>translate</c> attribute.
/// </summary>
public class TranslateValues
{
    /// <summary>
    /// Gets the predefined <c>no</c> attribute value, indicating that the element should not be translated.
    /// </summary>
    public string No => HtmlAttributeValues.No;

    /// <summary>
    /// Gets the predefined <c>yes</c> attribute value, indicating that the element should be translated.
    /// </summary>
    public string Yes => HtmlAttributeValues.Yes;
}