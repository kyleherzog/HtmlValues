namespace WebIdentifiers.Html.Attributes;

/// /// <summary>
/// Provides access to the attributes of the body element.
/// </summary>
public class BodyAttributes : GlobalAttributes
{
    /// <summary>
    /// Gets the name of the onafterprint attribute.
    /// </summary>
    public string OnAfterPrint => HtmlAttributes.OnAfterPrint;

    /// <summary>
    /// Gets the name of the onbeforeprint attribute.
    /// </summary>
    public string OnBeforePrint => HtmlAttributes.OnBeforePrint;

    /// <summary>
    /// Gets the name of the onbeforeunload attribute.
    /// </summary>
    public string OnBeforeUnload => HtmlAttributes.OnBeforeUnload;

    /// <summary>
    /// Gets the name of the onerror attribute.
    /// </summary>
    public string OnError => HtmlAttributes.OnError;

    /// <summary>
    /// Gets the name of the onhashchange attribute.
    /// </summary>
    public string OnHashChange => HtmlAttributes.OnHashChange;

    /// <summary>
    /// Gets the name of the onlanguagechange attribute.
    /// </summary>
    public string OnLanguageChange => HtmlAttributes.OnLanguageChange;

    /// <summary>
    /// Gets the name of the onload attribute.
    /// </summary>
    public string OnLoad => HtmlAttributes.OnLoad;

    /// <summary>
    /// Gets the name of the onmessage attribute.
    /// </summary>
    public string OnMessage => HtmlAttributes.OnMessage;

    /// <summary>
    /// Gets the name of the onoffline attribute.
    /// </summary>
    public string OnOffline => HtmlAttributes.OnOffline;

    /// <summary>
    /// Gets the name of the ononline attribute.
    /// </summary>
    public string OnOnline => HtmlAttributes.OnOnline;

    /// <summary>
    /// Gets the name of the onpagehide attribute.
    /// </summary>
    public string OnPageHide => HtmlAttributes.OnPageHide;

    /// <summary>
    /// Gets the name of the onpageshow attribute.
    /// </summary>
    public string OnPageShow => HtmlAttributes.OnPageShow;

    /// <summary>
    /// Gets the name of the onpopstate attribute.
    /// </summary>
    public string OnPopState => HtmlAttributes.OnPopState;

    /// <summary>
    /// Gets the name of the onrejectionhandled attribute.
    /// </summary>
    public string OnRejectionHandled => HtmlAttributes.OnRejectionHandled;

    /// <summary>
    /// Gets the name of the onresize attribute.
    /// </summary>
    public string OnResize => HtmlAttributes.OnResize;

    /// <summary>
    /// Gets the name of the onstorage attribute.
    /// </summary>
    public string OnStorage => HtmlAttributes.OnStorage;

    /// <summary>
    /// Gets the name of the onunhandledrejection attribute.
    /// </summary>
    public string OnUnhandledRejection => HtmlAttributes.OnUnhandledRejection;

    /// <summary>
    /// Gets the name of the onunload attribute.
    /// </summary>
    public string OnUnload => HtmlAttributes.OnUnload;
}