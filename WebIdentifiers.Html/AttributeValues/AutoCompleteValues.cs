namespace WebIdentifiers.Html.AttributeValues;

/// <summary>
/// Provides access to predefined values for the <c>autocomplete</c> attribute.
/// </summary>
public class AutoCompleteValues
{
    /// <summary>
    /// Gets the predefined <c>off</c> attribute value.
    /// </summary>
    public string Off => HtmlAttributeValues.Off;

    /// <summary>
    /// Gets the predefined <c>on</c> attribute value.
    /// </summary>
    public string On => HtmlAttributeValues.On;
}