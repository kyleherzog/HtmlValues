namespace WebIdentifiers.Html.AttributeValues;

/// <summary>
/// Provides access to predefined values for the <c>shape</c> attribute.
/// </summary>
public class ShapeValues
{
    /// <summary>
    /// Gets the predefined <c>default</c> attribute value.
    /// </summary>
    public string Default => HtmlAttributeValues.Default;

    /// <summary>
    /// Gets the predefined <c>rect</c> attribute value.
    /// </summary>
    public string Rect => HtmlAttributeValues.Rect;

    /// <summary>
    /// Gets the predefined <c>circle</c> attribute value.
    /// </summary>
    public string Circle => HtmlAttributeValues.Circle;

    /// <summary>
    /// Gets the predefined <c>poly</c> attribute value.
    /// </summary>
    public string Poly => HtmlAttributeValues.Poly;
}