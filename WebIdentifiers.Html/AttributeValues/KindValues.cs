namespace WebIdentifiers.Html.AttributeValues;

/// <summary>
/// Provides access to predefined values of the <c>kind</c> attribute.
/// </summary>
public class KindValues
{
    /// <summary>
    /// Gets the predefined <c>captions</c> attribute value.
    /// </summary>
    public string Captions => HtmlAttributeValues.Captions;

    /// <summary>
    /// Gets the predefined <c>chapters</c> attribute value.
    /// </summary>
    public string Chapters => HtmlAttributeValues.Chapters;

    /// <summary>
    /// Gets the predefined <c>descriptions</c> attribute value.
    /// </summary>
    public string Descriptions => HtmlAttributeValues.Descriptions;

    /// <summary>
    /// Gets the predefined <c>metadata</c> attribute value.
    /// </summary>
    public string Metadata => HtmlAttributeValues.Metadata;

    /// <summary>
    /// Gets the predefined <c>subtitles</c> attribute value.
    /// </summary>
    public string Subtitles => HtmlAttributeValues.Subtitles;
}