namespace WebIdentifiers.Html.AttributeValues;

/// <summary>
/// Provides access to predefined values of the <c>preload</c> attribute.
/// </summary>
public class PreloadValues
{
    /// <summary>
    /// Gets the predefined <c>auto</c> attribute value.
    /// </summary>
    public string Auto => HtmlAttributeValues.Auto;

    /// <summary>
    /// Gets the predefined <c>metadata</c> attribute value.
    /// </summary>
    public string Metadata => HtmlAttributeValues.Metadata;

    /// <summary>
    /// Gets the predefined <c>none</c> attribute value.
    /// </summary>
    public string None => HtmlAttributeValues.None;
}